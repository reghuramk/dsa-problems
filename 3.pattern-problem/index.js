const nbynstarpattern = (n) => {
  let pattern = "";
  for (i = 0; i < n; i++) {
    for (j = 0; j < n; j++) {
      pattern += " *";
    }
    pattern += "\n";
  }
  return pattern;
};


const trianglePattern = (n) => {
  let pattern = "";
  for (i = 0; i < n; i++) {
    for (j = 0; j < i; j++) {
      pattern += "*";
    }
    pattern += "\n";
  }
  return pattern;
};

